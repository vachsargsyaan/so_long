#include	"so_long.h"

void	ft_search2(char **str)
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	while (str[i])
	{
		j = 1;
		while (str[i][j])
		{
			if (str[i][j] == 'E')
			{
				if (str[i + 1][j] != '-' && str[i - 1][j] != '-' &&
					str[i][j - 1] != '-' && str[i][j + 1] != '-')
					ft_error();
			}
			j++;
		}
		i++;
	}
}
